    public boolean validar(){
    boolean r=false;
    if(txtTitulo.getText().equals("") || txtAlumno.getText().equals("") || txtObs.getText().equals("") || txtCarrera.getText().equals("") || txtFormato.getText().equals("")){
      JOptionPane.showMessageDialog(null,"Favor de llenar todos los campos","Campos vacios",JOptionPane.ERROR_MESSAGE);
    }else{
    r=true;
    }    
    return r;
    }
    

