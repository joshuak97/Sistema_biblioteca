    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //este metodo ejecutara el metodo agregar nuevo libro o editarLibro segun sea el caso:
        int area_id=validarArea(txtArea.getText());
        
        if(area_id!=0){
        if(operacion.equals("nuevo")){
            if(lblId.getText().equals("")){
            nuevoLibro();
            if(dl.isValidarRegistoLibro()){
            libroId=generarLibroId();
            int nEjemplares=Integer.parseInt(txtEjemplares.getText());
            nuevoEjemplar(nEjemplares, libroId);
            }
            ImageIcon icon=new ImageIcon(getClass().getResource("/imagenes/bien.png"));
            JOptionPane.showMessageDialog(null,"Se han registrado: "+con+" Ejemplares","Operación Exitosa",JOptionPane.INFORMATION_MESSAGE,icon);
            }else{
            libroId=generarLibroId();
            int nEjemplares=Integer.parseInt(txtEjemplares.getText());
            nuevoEjemplar(nEjemplares, libroId);
            ImageIcon icon=new ImageIcon(getClass().getResource("/imagenes/bien.png"));
            JOptionPane.showMessageDialog(null,"Se han registrado: "+con+" Ejemplares","Operación Exitosa",JOptionPane.INFORMATION_MESSAGE,icon);
        }
        
        }if(operacion.equals("editar")){
          editarLibro(area_id);
          limpiar();
          deshabilitar();
        }
        }
        mostrarLibros();
        con=0;
        limpiar();
        operacion="";
        deshabilitar();
    }//GEN-LAST:event_btnGuardarActionPerformed

