    //Este metodo nos permitira realizar una consulta que nos generara el id del libro a la hora de dar de alta ejemplares:
    public int generarLibroId(){
    int dato=0;
    try{
    String sql="SELECT libro_id FROM Libro WHERE titulo="+'"'+txtTitulo.getText()+'"'+" AND autor="+'"'+txtAutor.getText()+'"'+"AND isbn="+'"'+txtIsbn.getText()+'"'+" AND editorial="+'"'+txtEditorial.getText()+'"'+" AND edicion="+'"'+txtEdicion.getText()+'"';
            PreparedStatement pst= (PreparedStatement) cn.prepareStatement(sql);
            ResultSet rst = pst.executeQuery();
            if(rst.next()){
            dato=rst.getInt("libro_id");
            }
    }catch(SQLException ex){
      JOptionPane.showMessageDialog(null,"Error al realizar la consulta"+ex,"ERROR", JOptionPane.ERROR_MESSAGE);
    
    }
    return dato;
    }

