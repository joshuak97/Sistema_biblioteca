   public void agregarUsuario(){
   if(String.valueOf(txtConfirmar.getPassword()).equals(String.valueOf(txtContrasena.getPassword()))){
   Lusuarios l=new Lusuarios();
   DUsuarios du=new DUsuarios();
   String usuario=txtUsuario.getText();
   if(du.consultarUsuario(usuario)){
   l.setIdUsuario(0);
   l.setNombre(txtNombre.getText());
   l.setaPaterno(txtApaterno.getText());
   l.setaMaterno(txtAmaterno.getText());
   l.setUsuario(txtUsuario.getText());
   l.setClave(String.valueOf(txtContrasena.getPassword()));
   l.setCorreo(txtCorreo.getText());
   l.setTelefono(txtTelefono.getText());
   l.setPerfil(comboPerfil.getSelectedItem().toString().toUpperCase());
   l.setPreguntaSeguridad(comboPS.getSelectedItem().toString());
   l.setRespuestaPS(txtRespuestaPS.getText());
   l.setEstado("ALTA");                                                                                                                      
   du.agregarUsuario(l);
   mostrarUsuarios();
   }else{
   JOptionPane.showMessageDialog(null,"El usuario "+usuario+" ya ha sido registrado");
   }
   }else{
   JOptionPane.showMessageDialog(null,"Las contrase単as no coinciden","Error",JOptionPane.ERROR_MESSAGE);
   }
   }
   
