    public void buscar(){
    
    DUsuarios du=new DUsuarios();        
    String consulta="";
    //  
    if(comboBuscar.getSelectedItem().toString().equals("Buscar por Usuario:")){
     consulta="usuario";
    }
    if(comboBuscar.getSelectedItem().toString().equals("Buscar por Perfil:")){
    consulta="perfil";
    }
    if(comboBuscar.getSelectedItem().toString().equals("Buscar por Nombre:")){
    consulta="nombre";
    }
    
    if(comboBuscar.getSelectedItem().toString().equals("Buscar por Ap. Paterno:")){
    consulta="apaterno";
    }
    if(comboBuscar.getSelectedItem().toString().equals("Buscar por Ap. Materno:")){
    consulta="amaterno";
    }
    if(comboBuscar.getSelectedItem().toString().equals("Buscar por Id:")){
    consulta="idusuario";
    }
    DefaultTableModel modelo=du.consultar(consulta,txtBuscar.getText());
    tblUsuarios.setModel(modelo);
    }
    
    public void seleccionarPerfil(String perfil){
    
    if(perfil.equals("ENCARGADO")){
         comboPerfil.setSelectedItem(0);
         comboPerfil.setSelectedIndex(0);
        }else{
        comboPerfil.setSelectedItem(1);
         comboPerfil.setSelectedIndex(1);
        }    
    
    }
    public void seleccionarPS(String pregunta){
       
        if(pregunta.equals("Personaje favorito?")){
        
        comboPS.setSelectedItem(0);
        comboPS.setSelectedIndex(0);    
            
        }else if(pregunta.equals("Cumplea単os de Mama?")){
        comboPS.setSelectedItem(1);
        comboPS.setSelectedIndex(1);
        }else if(pregunta.equals("Nombre de mi Primera Mascota?")){
        comboPS.setSelectedItem(2);
        comboPS.setSelectedIndex(2);
        }else if(pregunta.equals("Mejor amigo de la infancia?")){
        comboPS.setSelectedItem(3);
        comboPS.setSelectedIndex(3);
        }else if(pregunta.equals("Lugar de Nacimiento?")){
        
        comboPS.setSelectedItem(4);
        comboPS.setSelectedIndex(4);
        }else{
        JOptionPane.showMessageDialog(null,"Pregunta seleccionada invalida","Error",JOptionPane.ERROR_MESSAGE);
        }
    }
