    public String extraerCombo(){
    String consulta=null,combo;
    
    combo=comboBuscar.getSelectedItem().toString();
    
    if(combo.equals("Buscar por Folio:")){
    consulta="id_unico";
    }if(combo.equals("Buscar por Titulo:")){
    consulta="titulo";
    }    
    if(combo.equals("Buscar por ISBN:")){
     consulta="isbn";   
    }
    if(combo.equals("Buscar por Autor:")){
     consulta="autor";      
    }
    if(combo.equals("Buscar por Editorial:")){
    consulta="editorial";
    }
    if(combo.equals("Buscar por Edicion:")){
    consulta="edicion";
    }
    if(combo.equals("Buscar por Estado:")){
    consulta="estadoLibro";
    }
    if(combo.equals("Buscar por Area:")){
    consulta="nombre_area";
    }
    return consulta;
    }
    
    public void buscar(){
    Dlibros dl=new Dlibros();
    String consulta=extraerCombo();
    DefaultTableModel modelo=dl.consultar(consulta,txtBuscar.getText());
    tblConsultaLibros.setModel(modelo);
    lblTotalLibros.setText("Se encontraron: "+dl.getTotalLibros()+" Resultados");
    }
